# Para descobrir qual lampâda está ligada ao seu respectivo interruptor é necessário seguir alguns passos:
# 1. Ir a sala de interruptores;
# 2. Acender um primeiro interruptor durante alguns minutos;
# 3. Apagar o primeiro interruptor e acender um segundo, retornar a sala das lâmpadas;
# 4. Verificar a lampâda que está acesa, pois estará conectada ao primeiro interruptor e verificar qual lampâda está apagada porém quente,
# pois irá indicar a lampâda que foi acesa no primeiro interruptor;
# 5. Verificar a lampâda que não está acesa e está fria, pois indicará a lampâda a qual o terceiro interruptor está ligado, pois ele não
# foi acionado.